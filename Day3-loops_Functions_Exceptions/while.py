

print("1. Print numbers from 1 to 5")
i = 1
while i <= 5:
    print(i)
    i += 1

print("\n2. Sum of first 5 natural numbers")
i = 1
total = 0
while i <= 5:
    total += i
    i += 1
print("Sum =", total)

print("\n3. Multiplication table of 5")
i = 1
while i <= 10:
    print("5 x", i, "=", 5 * i)
    i += 1

print("\n4. Simple star pattern")
i = 1
while i <= 5:
    print("*" * i)
    i += 1
