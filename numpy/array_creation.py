import numpy as np

# Creating arrays
arr1 = np.array([1, 2, 3, 4, 5])
arr2 = np.zeros((3, 3))
arr3 = np.ones((2, 4))
arr4 = np.arange(10)
arr5 = np.linspace(0, 1, 5)

print("Array 1:", arr1)
print("Zeros array:", arr2)
print("Ones array:", arr3)
print("Arange array:", arr4)
print("Linspace array:", arr5)
